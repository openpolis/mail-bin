def get_env_variable(var_name, *defaults):
  """ Get the environment variable or return exception """
  try:
      return os.environ[var_name]
  except KeyError:
      if not defaults:
          error_msg = "Set the %s environment variable" % var_name
          raise ImproperlyConfigured(error_msg)
      else:
          return defaults[0]


